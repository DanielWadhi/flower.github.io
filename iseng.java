public class PerkenalanDiri {
    public static void main(String[] args) {
        // Variabel dengan tipe data String untuk nama
        String nama = "John Doe";

        // Variabel dengan tipe data integer untuk umur
        int umur = 25;

        // Variabel dengan tipe data float untuk tinggi badan
        float tinggiBadan = 175.5f;

        // Menampilkan perkenalan diri menggunakan variabel-variabel di atas
        System.out.println("Halo, nama saya " + nama + ".");
        System.out.println("Saya berumur " + umur + " tahun.");
        System.out.println("Tinggi badan saya adalah " + tinggiBadan + " cm.");
    }
}